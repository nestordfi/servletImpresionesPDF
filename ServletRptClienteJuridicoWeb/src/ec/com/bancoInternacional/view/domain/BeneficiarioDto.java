package ec.com.bancoInternacional.view.domain;

import java.io.Serializable;

@SuppressWarnings("serial")
public class BeneficiarioDto implements Serializable {
	private static final long serialVersionUID = -6726559327275298582L;

	private Integer countBeneficiario;
	private String tipo;
	private String identificacion;
	private String nacionalidad;
	private String nombres;
	
	public BeneficiarioDto() {
		super();
	}
	
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getIdentificacion() {
		return identificacion;
	}
	public void setIdentificacion(String identificacion) {
		this.identificacion = identificacion;
	}
	public String getNacionalidad() {
		return nacionalidad;
	}
	public void setNacionalidad(String nacionalidad) {
		this.nacionalidad = nacionalidad;
	}
	public String getNombres() {
		return nombres;
	}
	public void setNombres(String nombres) {
		this.nombres = nombres;
	}

	public Integer getCountBeneficiario() {
		return countBeneficiario;
	}

	public void setCountBeneficiario(Integer countBeneficiario) {
		this.countBeneficiario = countBeneficiario;
	}
	
	
}
