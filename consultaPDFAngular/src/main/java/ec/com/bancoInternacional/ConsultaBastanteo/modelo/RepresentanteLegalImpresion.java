public class RepresentanteLegalImpresion {

	private String tipoIdentificacionRep;
	private String identificacionRep;
	private String nacionalidadRep;
	private String nombreApellidoRep;
	private String cargoRep;
	private String indexRep;
	private String fechaInscripcionRep;
	private String fechaVencimientoRep;
	public String getTipoIdentificacionRep() {
		return tipoIdentificacionRep;
	}
	public void setTipoIdentificacionRep(String tipoIdentificacionRep) {
		this.tipoIdentificacionRep = tipoIdentificacionRep;
	}
	public String getIdentificacionRep() {
		return identificacionRep;
	}
	public void setIdentificacionRep(String identificacionRep) {
		this.identificacionRep = identificacionRep;
	}
	public String getNacionalidadRep() {
		return nacionalidadRep;
	}
	public void setNacionalidadRep(String nacionalidadRep) {
		this.nacionalidadRep = nacionalidadRep;
	}
	public String getNombreApellidoRep() {
		return nombreApellidoRep;
	}
	public void setNombreApellidoRep(String nombreApellidoRep) {
		this.nombreApellidoRep = nombreApellidoRep;
	}
	public String getCargoRep() {
		return cargoRep;
	}
	public void setCargoRep(String cargoRep) {
		this.cargoRep = cargoRep;
	}
	public String getIndexRep() {
		return indexRep;
	}
	public void setIndexRep(String indexRep) {
		this.indexRep = indexRep;
	}
	public String getFechaInscripcionRep() {
		return fechaInscripcionRep;
	}
	public void setFechaInscripcionRep(String fechaInscripcionRep) {
		this.fechaInscripcionRep = fechaInscripcionRep;
	}
	public String getFechaVencimientoRep() {
		return fechaVencimientoRep;
	}
	public void setFechaVencimientoRep(String fechaVencimientoRep) {
		this.fechaVencimientoRep = fechaVencimientoRep;
	}
}
