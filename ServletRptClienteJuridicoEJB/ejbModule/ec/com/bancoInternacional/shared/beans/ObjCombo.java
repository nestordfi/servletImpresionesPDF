package ec.com.bancoInternacional.shared.beans;

import java.io.Serializable;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

public class ObjCombo implements Serializable {
	private static final long serialVersionUID = -7606513304685191774L;
	
	private Map<String,List<String>> listas = null;
	private Map<String,Map<String, String>> mapas = null;
	
	public ObjCombo(){
		super();
		listas = new HashMap<String,List<String>>();
		mapas = new HashMap<String,Map<String, String>>();
	}

	public Map<String, List<String>> getListas() {
		return listas;
	}

	public void setListas(Map<String, List<String>> listas) {
		this.listas = listas;
	}

	public Map<String, Map<String, String>> getMapas() {
		return mapas;
	}

	public void setMapas(Map<String, Map<String, String>> mapas) {
		this.mapas = mapas;
	}

}//fin de la clase