package ec.com.bancoInternacional.shared.beans;

public class ObjCliente {
	private static final long serialVersionUID = 1861555579102478691L;

	private String id;
	private String nroIbs;
	private String tipo;
	private String value;
	private String nombreCorto;
	private String transaccion;

	public ObjCliente() {
		super();
	}

	public ObjCliente(String id, String nroIbs, String tipo, String value, String nombreCorto, String transaccion) {
		super();
		setId(id);
		setNroIbs(nroIbs);
		setTipo(tipo);
		setValue(value);
		setNombreCorto(nombreCorto);
		setTransaccion(transaccion);
	}// ObjCliente

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNroIbs() {
		return nroIbs;
	}

	public void setNroIbs(String nroIbs) {
		this.nroIbs = nroIbs;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getNombreCorto() {
		return nombreCorto;
	}

	public void setNombreCorto(String nombreCorto) {
		this.nombreCorto = nombreCorto;
	}

	public String getTransaccion() {
		return transaccion;
	}

	public void setTransaccion(String transaccion) {
		this.transaccion = transaccion;
	}

}// fin de la clase