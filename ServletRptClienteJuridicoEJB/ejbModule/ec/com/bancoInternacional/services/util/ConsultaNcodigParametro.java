package ec.com.bancoInternacional.services.util;

import java.io.Serializable;

/**
 * Autor: Sandro Guevara
 * Objetivo:	Clase que encapsula los parametros de Consulta hacia NCODIG
 * Fecha: 08-05-2018 
 * Nro. Req: 1605 
 * Version: 1.0
 */
public class ConsultaNcodigParametro  implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String tabla;
	private String nivel;
	private String cod1;
	private String cod2;
	private String cod3;
	private String cod4;

	public ConsultaNcodigParametro(String tabla, String nivel, String cod1, String cod2, String cod3, String cod4) {
		this.tabla = tabla;
		this.nivel = nivel;
		this.cod1 = cod1;
		this.cod2 = cod2;
		this.cod3 = cod3;
		this.cod4 = cod4;
	}

	public String getTabla() {
		return tabla;
	}

	public void setTabla(String tabla) {
		this.tabla = tabla;
	}

	public String getNivel() {
		return nivel;
	}

	public void setNivel(String nivel) {
		this.nivel = nivel;
	}

	public String getCod1() {
		return cod1;
	}

	public void setCod1(String cod1) {
		this.cod1 = cod1;
	}

	public String getCod2() {
		return cod2;
	}

	public void setCod2(String cod2) {
		this.cod2 = cod2;
	}

	public String getCod3() {
		return cod3;
	}

	public void setCod3(String cod3) {
		this.cod3 = cod3;
	}

	public String getCod4() {
		return cod4;
	}

	public void setCod4(String cod4) {
		this.cod4 = cod4;
	}
}